using System;
using ChainOfResponsibility.YT_CheckUser.Common;

namespace ChainOfResponsibility.YT_CheckUser.Main
{
    public class Main
    {
        class MainClass
        {
            public static void Main(string[] args)
            {
                var user = new User();
                var id = new CheckId();
                var name  = new CheckName();
                var password = new CheckPassword();

                id.Then(name);
                name.Then(password);

                id.Check(user);
            }
        }
    }
}
